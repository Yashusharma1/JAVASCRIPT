// if and else statement

const sarahAge = 19;
const canDrive = sarahAge >= 18;

if (canDrive) {
  console.log(`sarah can drive the car`);
} else {
  console.log(`sarah cannot drive the car as her age is ${sarahAge - 18} `);
}

